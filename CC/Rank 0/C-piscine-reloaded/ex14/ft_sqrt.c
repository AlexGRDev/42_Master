int	ft_sqrt(int nbr)
{
	long	i;

	if (nbr <= 0)
		return (0);
	i = 1;
	while (i * i <= nbr)
	{
		if (i * i == nbr)
			return ((int)(i));
		i++;
	}
	return (0);
}
