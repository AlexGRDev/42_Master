#include "libft.h"

int	ft_strlcat(char *dst, char *src, size_t size)
{
	char	*d;
	char	*s;
	size_t	n;
	size_t	dlen;

	d = dst;
	s = src;
	n = size;
	while (n-- != 0 && *d != '\0')
		(*d)++;
	dlen = d - dst;
	n = size - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s != '\0')
	{
		*(d)++ = *(s)++;
		n--;
	}
	*d = '\0';
	return (dlen + (s - src));
}
