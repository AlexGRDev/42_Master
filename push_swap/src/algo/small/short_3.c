#include "push_swap.h"

void	sort_3(t_stack *s)
{
	int	a;
	int	b;
	int	c;

	a = s->a[0];
	b = s->a[1];
	c = s->a[2];
	if (a > b && a > c)
		ra(s);
	else if (b > a && b > c)
		rra(s);
	if (s->a[0] > s->a[1])
		sa(s);
}
